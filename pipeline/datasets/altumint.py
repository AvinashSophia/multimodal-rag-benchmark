"""Altumint FLVM dataset loader for multimodal technical document QA.

The Altumint dataset consists of 7 proprietary PDF documents covering the
Flashing Light Video Monitor (FLVM) product: assembly instructions, configuration
manuals, engineering drawings, continuity check tables, wire length tables, and
a production wiring diagram.

Corpus structure:
- 36 pages across 7 PDFs, each page = one text chunk + one page screenshot
- Page IDs follow the slugified pattern from parse_documents.py:
  e.g. altumint_dc001_01_flvm_electronics_enclosure_assembly_drawing_p01
- Text corpus is sourced from full_text in per-page parsed JSONs
- Image corpus is sourced from page_screenshot PNGs in per-page parsed JSONs
- QA pairs are pre-generated by scripts/generate_qa.py and stored in
  data/altumint/qa_pairs.json

Two query types are supported:
  text   — UnifiedSample.images = [] (no query image; text-only retrieval)
  visual — UnifiedSample.images = [PIL.Image] (diagram crop used as query image)

Config keys (under dataset):
    data_dir:    Path to data/ directory (default: data/)
    max_samples: Optional limit on QA pairs loaded (default: all)

To regenerate parsed JSONs:
    uv run python scripts/parse_documents.py --input data/altumint/

To regenerate QA pairs:
    uv run python scripts/generate_qa.py --input data/altumint/parsed/
"""

import json
from pathlib import Path
from typing import Any, Dict, List, Optional, Tuple

from PIL import Image

from pipeline.datasets.base import BaseDataset, register_dataset
from pipeline.utils import UnifiedSample


@register_dataset("altumint")
class AltumintDataset(BaseDataset):
    """Altumint FLVM technical document QA dataset.

    Each sample is a QA pair generated from a specific page of a proprietary
    PDF document. The retrieval corpus is built from Docling-parsed per-page
    JSONs in data/altumint/parsed/.

    Supports two query types:
      text   — pure text query; CLIP image retrieval is not used
      visual — diagram crop as query image; tests cross-modal retrieval
    """

    def __init__(self, config: Dict[str, Any]):
        super().__init__(config)
        self.altumint_dir = Path(self.data_dir) / "altumint"
        self.parsed_dir = self.altumint_dir / "parsed"
        self.qa_file = self.altumint_dir / "qa_pairs.json"

        # Populated lazily in get_corpus() / get_images()
        self._corpus_texts: Optional[List[str]] = None
        self._corpus_ids: Optional[List[str]] = None
        self._corpus_images: Optional[List[Image.Image]] = None
        self._corpus_image_ids: Optional[List[str]] = None

    # ------------------------------------------------------------------
    # Helpers
    # ------------------------------------------------------------------

    def _build_corpus(self) -> None:
        """Build text and image corpus caches from parsed page JSONs."""
        if self._corpus_texts is not None:
            return  # Already built

        if not self.parsed_dir.exists():
            raise FileNotFoundError(
                f"Parsed directory not found: {self.parsed_dir}\n"
                f"Run: uv run python scripts/parse_documents.py --input {self.altumint_dir}/"
            )

        texts: List[str] = []
        text_ids: List[str] = []
        images: List[Image.Image] = []
        image_ids: List[str] = []

        for page_file in sorted(self.parsed_dir.glob("*.json")):
            page = json.loads(page_file.read_text())
            page_id = page["page_id"]

            # Text corpus: full_text + table summaries and markdown
            # Tables are separate from text_blocks in Docling output so full_text
            # alone misses all table content (e.g. DC004 continuity checks, DC005 wire lengths)
            corpus_text = page.get("full_text", "")
            for tbl in page.get("tables", []):
                if tbl.get("summary"):
                    corpus_text += "\n" + tbl["summary"]
                if tbl.get("markdown"):
                    corpus_text += "\n" + tbl["markdown"]
            texts.append(corpus_text)
            text_ids.append(page_id)

            # Image corpus: page screenshot PNG
            screenshot = next(
                (f for f in page.get("figures", []) if f.get("label") == "page_screenshot"),
                None,
            )
            if screenshot and Path(screenshot["path"]).exists():
                images.append(Image.open(screenshot["path"]).convert("RGB"))
            else:
                # Fallback: blank placeholder so index stays aligned with text corpus
                images.append(Image.new("RGB", (100, 100), color=(255, 255, 255)))
            image_ids.append(page_id)

        self._corpus_texts = texts
        self._corpus_ids = text_ids
        self._corpus_images = images
        self._corpus_image_ids = image_ids

        print(f"  [Altumint] Corpus built: {len(texts)} pages from {self.parsed_dir}")

    # ------------------------------------------------------------------
    # BaseDataset interface
    # ------------------------------------------------------------------

    def load(self) -> None:
        """Load QA pairs from qa_pairs.json and build UnifiedSamples."""
        if not self.qa_file.exists():
            raise FileNotFoundError(
                f"QA pairs file not found: {self.qa_file}\n"
                f"Run: uv run python scripts/generate_qa.py --input {self.parsed_dir}/"
            )

        with open(self.qa_file) as f:
            qa_pairs: List[Dict[str, Any]] = json.load(f)

        if self.max_samples:
            qa_pairs = qa_pairs[: self.max_samples]

        self._build_corpus()

        for qa in qa_pairs:
            query_type = qa.get("query_type", "text")
            query_image_path = qa.get("query_image_path")

            # Build query images list based on query type
            if query_type == "visual" and query_image_path:
                img_path = Path(query_image_path)
                if img_path.exists():
                    query_images: List[Image.Image] = [Image.open(img_path).convert("RGB")]
                    # Use source_page_id so it matches the CLIP corpus IDs (page screenshots)
                    query_image_ids: List[str] = [qa["source_page_id"]]
                else:
                    # Missing figure — fall back to text query
                    query_images = []
                    query_image_ids = []
            else:
                query_images = []
                query_image_ids = []

            sample = UnifiedSample(
                id=qa["id"],
                question=qa["question"],
                text_corpus=[],   # full corpus provided via get_corpus()
                images=query_images,
                image_ids=query_image_ids,
                ground_truth=qa["answer"],
                metadata={
                    "source_doc":         qa["source_doc"],
                    "source_page":        qa["source_page"],
                    "source_page_id":     qa["source_page_id"],
                    "query_type":         query_type,
                    "question_type":      qa.get("question_type", "factual"),
                    "all_answers":        [qa["answer"]],
                    "relevant_text_ids":  qa.get("relevant_page_ids", [qa["source_page_id"]]),
                    "relevant_image_ids": qa.get("relevant_page_ids", [qa["source_page_id"]]),
                },
            )
            self.samples.append(sample)

        text_count = sum(1 for qa in qa_pairs if qa.get("query_type", "text") == "text")
        visual_count = sum(1 for qa in qa_pairs if qa.get("query_type") == "visual")
        print(f"  [Altumint] Loaded {len(self.samples)} QA samples "
              f"({text_count} text, {visual_count} visual)")

    def get_corpus(self) -> Tuple[List[str], List[str]]:
        """Return all page full_text values and their page IDs for text retrieval."""
        self._build_corpus()
        assert self._corpus_texts is not None and self._corpus_ids is not None
        return self._corpus_texts, self._corpus_ids

    def get_images(self) -> Tuple[List[Image.Image], List[str]]:
        """Return all page screenshot images and their page IDs for CLIP indexing."""
        self._build_corpus()
        assert self._corpus_images is not None and self._corpus_image_ids is not None
        return self._corpus_images, self._corpus_image_ids
